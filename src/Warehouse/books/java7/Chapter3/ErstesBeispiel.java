package Warehouse.books.java7.Chapter3;

public class ErstesBeispiel {
    public static void main(String[] args) {
        int ersteZahl;                           // Объявление
        int zweiteZahl;
        int ergebnis;

        ersteZahl = 8754;                        // Инициализация
        zweiteZahl = 398;

        ergebnis = ersteZahl - zweiteZahl;       // Применение
        System.out.println(" 8754 - 398  = " + ergebnis);
    }
}

